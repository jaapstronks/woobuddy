<h1>Algemene voorwaarden</h1>
<p class="lead">
	Deze voorwaarden gelden voor het gebruik van WOO Buddy, zowel via woobuddy.nl als zelf gehost
	vanuit de open-source code.
</p>
<p class="meta">Laatst bijgewerkt: 28 april 2026</p>

<h2>1. Wie zijn wij?</h2>
<p>
	WOO Buddy wordt aangeboden door Bureau Bolster B.V. (KvK 61487953), handelend onder de naam
	WOO Buddy. Contact:
	<a href="mailto:hallo@woobuddy.nl">hallo@woobuddy.nl</a>.
</p>

<h2>2. Wat is WOO Buddy?</h2>
<p>
	WOO Buddy is een hulpmiddel dat Nederlandse ambtenaren ondersteunt bij het lakken van
	persoonsgegevens in Woo-documenten. Detectie gebeurt op basis van regels (regex, Dutch NER,
	woordenlijsten, structuurheuristieken). Er wordt geen taalmodel (LLM) gebruikt.
</p>

<h2>3. Hulpmiddel, geen vervanging voor een jurist</h2>
<p>
	WOO Buddy is een <strong>hulpmiddel voor de reviewer</strong>, niet een vervanging. De
	eindverantwoordelijkheid voor het Woo-besluit en het gelakte document ligt altijd bij het
	bestuursorgaan en de verantwoordelijke jurist. Wij geven geen juridisch advies.
</p>

<h2>4. Geen garantie op volledigheid</h2>
<p>
	Regel-gebaseerde detectie herkent veel, maar niet alles. Een reviewer moet elke detectie
	beoordelen en zelf controleren of er persoonsgegevens gemist zijn. WOO Buddy geeft
	<strong>geen garantie</strong>
	dat alle persoonsgegevens zijn gevonden. Gebruik het als <em>versterking</em> van je workflow,
	niet als vervanging van menselijke beoordeling.
</p>

