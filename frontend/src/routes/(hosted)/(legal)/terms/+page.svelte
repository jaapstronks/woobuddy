	verantwoordelijkheid voor beveiliging, beschikbaarheid en naleving van de AVG.
</p>

<h2>6. Toegestaan gebruik</h2>
<p>Je mag WOO Buddy gebruiken voor:</p>
<ul>
	<li>het lakken van documenten in het kader van Woo-verzoeken;</li>
	<li>het verwerken van vergelijkbare privacygevoelige documenten binnen je organisatie.</li>
</ul>
<p>Je mag WOO Buddy <strong>niet</strong> gebruiken voor:</p>
<ul>
	<li>het omzeilen van beveiliging of misbruiken van de dienst;</li>
	<li>geautomatiseerde scraping of lastploeg-achtige belasting van onze infrastructuur;</li>
	<li>onrechtmatige doeleinden of schending van de rechten van derden.</li>
</ul>

<h2>7. Beschikbaarheid en onderhoud</h2>
<p>
	We doen ons best om de dienst beschikbaar te houden, maar geven geen SLA op de Gratis tier.
	Betaalde Team- en Enterprise-klanten kunnen een SLA afspreken in hun contract. Onderhoud kan
	leiden tot tijdelijke onderbrekingen; we proberen dit buiten kantooruren te doen.
</p>

<h2>8. Aansprakelijkheid</h2>
<p>
	Voor zover wettelijk toegestaan, is onze aansprakelijkheid beperkt tot het bedrag dat je in de
	voorgaande twaalf maanden aan ons hebt betaald. Voor gebruikers van de Gratis tier is dat dus
	nihil. We zijn niet aansprakelijk voor indirecte schade, gevolgschade, gederfde winst of verlies
	van gegevens.
</p>
<p>
	Deze beperkingen gelden niet bij opzet of bewuste roekeloosheid aan onze kant, of bij schending
	van dwingendrechtelijke bepalingen.
</p>

<h2>9. Betaalde abonnementen</h2>
<p>
	Team- en Enterprise-abonnementen worden maandelijks of jaarlijks gefactureerd. Opzegging kan per
	eerstvolgende factureerperiode. Prijzen zijn exclusief BTW. Details staan in je individuele
	overeenkomst.
</p>

<h2>10. Beëindiging</h2>
<p>
	Je kunt je account op elk moment beëindigen. Wij mogen het account beëindigen bij
	wanbetaling of bij ernstige schending van deze voorwaarden. Bij beëindiging verwijderen we je
	accountgegevens binnen 30 dagen.
</p>

<h2>11. Wijzigingen</h2>
<p>
	We mogen deze voorwaarden aanpassen. Substantiële wijzigingen kondigen we ten minste 30 dagen van
	tevoren aan. Als je het niet eens bent met de wijzigingen, kun je opzeggen.
</p>

<h2>12. Toepasselijk recht</h2>
<p>
	Op deze voorwaarden is Nederlands recht van toepassing. Geschillen worden voorgelegd aan de
	bevoegde rechter te Amsterdam, tenzij dwingend recht anders voorschrijft.
</p>
