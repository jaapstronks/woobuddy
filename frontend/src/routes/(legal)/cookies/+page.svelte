<h2>Welke cookies plaatsen we?</h2>

<h3>Functionele sessiecookie</h3>
<p>
	Bij inloggen plaatsen we één sessiecookie om je aangemelde status te onthouden. Deze cookie is
	<code>HttpOnly</code>, <code>Secure</code> en <code>SameSite=Lax</code>. Hij wordt verwijderd
	wanneer je uitlogt of na inactiviteit.
</p>

<h3>Voorkeuren (localStorage)</h3>
<p>
	De review-omgeving slaat interface-voorkeuren lokaal op in je browser (bijvoorbeeld welke tiers
	zichtbaar zijn). Dit zijn geen cookies in strikte zin, maar we noemen ze voor de volledigheid.
	Geen van deze gegevens verlaat je apparaat.
</p>

<h3>IndexedDB voor PDF-data</h3>
<p>
	Geüploade PDF's worden tijdelijk in IndexedDB opgeslagen, zodat je tijdens de review niet steeds
	opnieuw hoeft te uploaden. De bestanden blijven uitsluitend op jouw apparaat. Je kunt ze
	verwijderen door de browsercache voor deze site te wissen.
</p>

<h2>Wat we niet gebruiken</h2>
<ul>
	<li><strong>Geen reclame-cookies</strong> — we tonen geen advertenties.</li>
	<li><strong>Geen third-party trackers</strong> — geen Facebook Pixel, geen Google Analytics.</li>
	<li>
		<strong>Geen cookies voor analytics</strong> — we gebruiken Plausible Analytics, dat
		cookieloos en privacyvriendelijk werkt en geen persoonsgegevens verzamelt.
	</li>
	<li><strong>Geen A/B-testing trackers.</strong></li>
</ul>

<h2>Externe bronnen</h2>
<p>
	We laden geen fonts, scripts of stylesheets van externe domeinen. De interface gebruikt
	systeemlettertypes. Daardoor weten derde partijen niet dat je WOO Buddy bezoekt.
</p>

<h2>Cookies beheren</h2>
<p>
	Je kunt cookies op elk moment uitschakelen of verwijderen in je browserinstellingen. Als je de
	functionele sessiecookie blokkeert, kun je niet inloggen — het analysewerk op de landing page
	blijft wel gewoon werken zonder account.
</p>

